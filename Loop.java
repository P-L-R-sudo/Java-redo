public class Loop {
    public static void main(Strings[] args) {
        int i = 1;
        while (i <= 2) {
            System.out.println("Hi " + i);
            int j = 1;
            while (j <= 3) {
                System.out.println("Hello " + j);
                j++;
            }
            i++;
        }
    }
}
